const validateEmail = (mail) => {
  let validRegex = /^(([^<>()\[\]\\.,;:\s@"]+(\.[^<>()\[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
  let newEmail = String(mail).trim().toLowerCase();
  return validRegex.test(newEmail);
};
module.exports = { validateEmail };
